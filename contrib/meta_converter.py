code = {
        "<": "60",
        ">": "62",
        "(": "40",
        ")": "41",
        "[": "91",
        "]": "93",
}

cur = ""

if __name__ == '__main__':
    src = '(0)><>>><<[(0)><>>><<(60)<>>>>><>>><<(62)<>>>>><>(1)><><<<(0)><>>><<(40)<>>>>><>(1)><><<<(0)><>>><<(41)<>>>>><>(1)><><<<(0)><>>><<(91)<>>>>><>(1)><><<<(0)><>>><<(93)<>>>>><>(1)><><<<(0)><>(1)><>>><<[(0)><><><>><<(0)](1)><>><<>><<[(0)><><><>><>(0)](0)><>>><<]>>><<'
    out = []

    for char in src:
        if char in code:
            if cur != "":
                out.append("(" + str(cur) + ")")
                cur = ""

            out.append("("+code[char]+")")
        else:
            cur = cur + str(char)

    if cur != "":
        out.append("(" + str(cur) + ")")

    out.reverse()
    res = ''.join(out)
    print(res)
